function openServer(type) {
  if (type === "war") {
    alert("War System: Declare wars, manage alliances, track battles.");
  }

  if (type === "lifesteal") {
    alert("LifeSteal: Players steal hearts when killing others.");
  }

  if (type === "anarchy") {
    alert("Anarchy: No rules. Full chaos enabled.");
  }

  if (type === "peaceful") {
    alert("Peaceful: No PvP. Build freely.");
  }

  if (type === "template") {
    alert("Template: Create your own server mode.");
  }
}
